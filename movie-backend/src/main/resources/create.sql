CREATE TABLE IF NOT EXISTS MOVIE.MOVIE (
  ID BIGINT NOT NULL,
  CZECH_NAME VARCHAR(250),
  ORIG_NAME VARCHAR(250),
  GENRE BIGINT,
  DESCRIPTION VARCHAR(4096),
  COUNTRY BIGINT,
  IMAGE VARCHAR(250),
  LENGTH VARCHAR(5),
  YEAR VARCHAR(5),
  INS_USERS BIGINT,
  MODIF_USERS BIGINT,
